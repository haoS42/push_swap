#include "../include/push_swap.h"

t_stack	*find_last(t_stack *stack)
{
	if (!stack)
		return (NULL);
	while (stack->next)
		stack = stack->next;
	return (stack);
}

t_stack	*find_highest_node(t_stack *stack)
{
	int		max_index;
	t_stack	*highest_node;

	if (!stack)
		return (NULL);
	max_index = -1;
	highest_node = NULL;
	while (stack)
	{
		if (stack->index > max_index)
		{
			max_index = stack->index;
			highest_node = stack;
		}
		stack = stack->next;
	}
	return (highest_node);
}

int	stack_size(t_stack *stack)
{
	int	size;

	size = 0;
	while (stack)
	{
		size++;
		stack = stack->next;
	}
	return (size);
}

int	is_sorted(t_stack *stack)
{
	while (stack && stack->next)
	{
		if (stack->value > stack->next->value)
			return (0);
		stack = stack->next;
	}
	return (1);
}

void	free_stack(t_stack **stack)
{
	t_stack	*tmp;
	t_stack	*current;

	if (!stack)
		return ;
	current = *stack;
	while (current)
	{
		tmp = current->next;
		free(current);
		current = tmp;
	}
	*stack = NULL;
}
